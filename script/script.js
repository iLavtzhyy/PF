        // ========== CARGAR PRODUCTOS ==========
        function cargarProductos() {
            const contenedorHombre = document.getElementById('productosHombre');
            const contenedorOverzide = document.getElementById('productosoversize');
            const contenedorPromociones = document.getElementById('productosPromociones');

            if (contenedorHombre) {
                datosProductos.hombre.forEach((producto, indice) => {
                    contenedorHombre.innerHTML += crearTarjetaProducto(producto, indice * 0.1);
                });
            }

            if (contenedorOverzide) {
                datosProductos.overzide.forEach((producto, indice) => {
                    contenedorOverzide.innerHTML += crearTarjetaProducto(producto, indice * 0.1);
                });
            }

            if (contenedorPromociones) {
                datosProductos.promociones.forEach((producto, indice) => {
                    contenedorPromociones.innerHTML += crearTarjetaProducto(producto, indice * 0.1);
                });
            }
        }

        // ========== MENÚ HAMBURGUESA ==========
        const botonHamburguesa = document.getElementById('botonHamburguesa');
        const menuLateral = document.getElementById('menuLateral');
        const overlayMenu = document.getElementById('overlayMenu');

        // Abrir/cerrar menú
        function toggleMenu() {
            botonHamburguesa.classList.toggle('activo');
            menuLateral.classList.toggle('abierto');
            overlayMenu.classList.toggle('activo');
            document.body.style.overflow = menuLateral.classList.contains('abierto') ? 'hidden' : '';
        }

        botonHamburguesa.addEventListener('click', toggleMenu);
        overlayMenu.addEventListener('click', toggleMenu);

        // Cerrar menú al hacer clic en un enlace
        document.querySelectorAll('.menu-lateral .enlace-nav').forEach(enlace => {
            enlace.addEventListener('click', () => {
                toggleMenu();
            });
        });

        // ========== NAVEGACIÓN SUAVE ==========
        document.querySelectorAll('.enlace-nav').forEach(enlace => {
            enlace.addEventListener('click', function(e) {
                e.preventDefault();
                
                const idDestino = this.getAttribute('href');
                const seccionDestino = document.querySelector(idDestino);
                
                if (seccionDestino) {
                    const posicion = seccionDestino.offsetTop - 80;
                    window.scrollTo({
                        top: posicion,
                        behavior: 'smooth'
                    });
                }
            });
        });

        // ========== SCROLL TO TOP ==========
        function irArriba() {
            window.scrollTo({
                top: 0,
                behavior: 'smooth'
            });
        }

        // ========== EFECTOS DE SCROLL ==========
        let ultimoScroll = 0;
        window.addEventListener('scroll', () => {
            const navbar = document.getElementById('navbar');
            const botonScroll = document.getElementById('scrollArriba');
            const scrollActual = window.pageYOffset;

            // Mostrar botón scroll to top
            if (scrollActual > 300) {
                botonScroll.classList.add('visible');
            } else {
                botonScroll.classList.remove('visible');
            }

            // Ocultar navbar al bajar
            if (scrollActual > ultimoScroll && scrollActual > 500) {
                navbar.style.transform = 'translateY(-100%)';
            } else {
                navbar.style.transform = 'translateY(0)';
            }

            ultimoScroll = scrollActual;
        });

        // ========== INTERSECTION OBSERVER ==========
        const opcionesObservador = {
            threshold: 0.1,
            rootMargin: '0px 0px -50px 0px'
        };

        const observador = new IntersectionObserver((entradas) => {
            entradas.forEach(entrada => {
                if (entrada.isIntersecting) {
                    entrada.target.classList.add('visible');
                }
            });
        }, opcionesObservador);

        // Observar secciones
        document.addEventListener('DOMContentLoaded', () => {
            const secciones = document.querySelectorAll('.seccion');
            secciones.forEach(seccion => {
                observador.observe(seccion);
            });
        });

